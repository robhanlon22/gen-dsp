def _adapter_cpp_header_lines(platform: str, name: str) -> list[str]:
    return [
        f"// _ext_{platform}.cpp -- dsp-graph adapter for gen-dsp {platform} backend",
        "// Generated by dsp-graph",
        "",
        f'#include "gen_ext_common_{platform}.h"',
        "",
        "// Include dsp-graph compiled code",
        f'#include "{name}.cpp"',
        "",
        "typedef void GenState;",
        "",
        "namespace WRAPPER_NAMESPACE {",
        "",
    ]


def _adapter_cpp_lifecycle_lines(name: str, struct: str, st: str) -> list[str]:
    return [
        f"GenState* wrapper_create({st} sr, long bs) {{",
        "    (void)bs;",
        f"    return (GenState*){name}_create((float)sr);",
        "}",
        "",
        "void wrapper_destroy(GenState* state) {",
        f"    {name}_destroy(({struct}*)state);",
        "}",
        "",
        "void wrapper_reset(GenState* state) {",
        f"    {name}_reset(({struct}*)state);",
        "}",
        "",
    ]


def _adapter_cpp_perform_lines(
    name: str,
    struct: str,
    st: str,
    *,
    use_double: bool,
    io_counts: tuple[int, int],
) -> list[str]:
    num_inputs, num_outputs = io_counts
    lines: list[str] = []
    if use_double:
        lines.extend(
            [
                f"void wrapper_perform(GenState* state, {st}** ins, long numins,",
                f"                     {st}** outs, long numouts, long n) {{",
                "    (void)numins; (void)numouts;",
            ]
        )
        lines.extend([f"    float in{i}[n];" for i in range(num_inputs)])
        lines.extend([f"    float out{i}[n];" for i in range(num_outputs)])
        lines.append(f"    float* fins[{max(num_inputs, 1)}];")
        lines.append(f"    float* fouts[{max(num_outputs, 1)}];")
        lines.extend([f"    fins[{i}] = in{i};" for i in range(num_inputs)])
        lines.extend(
            [
                f"    for (long j = 0; j < n; j++) in{i}[j] = "
                f"(float)ins[{i}][j];"
                for i in range(num_inputs)
            ]
        )
        lines.extend([f"    fouts[{i}] = out{i};" for i in range(num_outputs)])
        lines.append(f"    {name}_perform(({struct}*)state, fins, fouts, (int)n);")
        lines.extend(
            [
                f"    for (long j = 0; j < n; j++) outs[{i}][j] = "
                f"(double)out{i}[j];"
                for i in range(num_outputs)
            ]
        )
    else:
        lines.extend(
            [
                f"void wrapper_perform(GenState* state, {st}** ins, long numins,",
                f"                     {st}** outs, long numouts, long n) {{",
                "    (void)numins; (void)numouts;",
                f"    {name}_perform(({struct}*)state, ins, outs, (int)n);",
            ]
        )
    lines.extend(["}", ""])
    return lines


def _adapter_cpp_introspection_lines(name: str, struct: str, st: str) -> list[str]:
    return [
        f"int wrapper_num_inputs() {{ return {name}_num_inputs(); }}",
        f"int wrapper_num_outputs() {{ return {name}_num_outputs(); }}",
        f"int wrapper_num_params() {{ return {name}_num_params(); }}",
        "",
        "const char* wrapper_param_name(GenState* state, int index) {",
        "    (void)state;",
        f"    return {name}_param_name(index);",
        "}",
        "",
        "const char* wrapper_param_units(GenState* state, int index) {",
        "    (void)state; (void)index;",
        '    return "";',
        "}",
        "",
        f"{st} wrapper_param_min(GenState* state, int index) {{",
        "    (void)state;",
        f"    return ({st}){name}_param_min(index);",
        "}",
        "",
        f"{st} wrapper_param_max(GenState* state, int index) {{",
        "    (void)state;",
        f"    return ({st}){name}_param_max(index);",
        "}",
        "",
        "char wrapper_param_hasminmax(GenState* state, int index) {",
        "    (void)state; (void)index;",
        "    return 1;",
        "}",
        "",
        f"void wrapper_set_param(GenState* state, int index, {st} value) {{",
        f"    {name}_set_param(({struct}*)state, index, (float)value);",
        "}",
        "",
        f"{st} wrapper_get_param(GenState* state, int index) {{",
        f"    return ({st}){name}_get_param(({struct}*)state, index);",
        "}",
        "",
    ]


def _adapter_cpp_buffers_lines(name: str) -> list[str]:
    return [
        f"int wrapper_num_buffers() {{ return {name}_num_buffers(); }}",
        "",
        "const char* wrapper_buffer_name(int index) {",
        f"    return {name}_buffer_name(index);",
        "}",
        "",
        "int wrapper_load_buffer(int index, const char* path) {",
        "    (void)index; (void)path;",
        "    return -1;  // graph-compiled code does not support runtime",
        "    // buffer loading",
        "}",
        "",
        "} // namespace WRAPPER_NAMESPACE",
        "",
    ]


def generate_adapter_cpp(graph: Graph, platform: str) -> str:
    """
    Generate the ``_ext_{platform}.cpp`` adapter source.

    This replaces the genlib-side wrapper with dsp-graph calls while
    keeping gen-dsp's platform-side code unchanged.

    Args:
        graph: Compiled DSP graph.
        platform: gen-dsp platform key (e.g. ``"chuck"``, ``"clap"``).

    Returns:
        Complete C++ source as a string.

    Raises:
        ValueError: If *platform* is not a recognised gen-dsp platform.

    """
    if platform not in _PLATFORM_INFO:
        msg = f"Unknown platform {platform!r}; supported: {sorted(_PLATFORM_INFO)}"
        raise ValueError(msg)

    name = graph.name
    struct = _to_pascal(name) + "State"
    use_double = platform == "max"
    st = "double" if use_double else "float"

    lines = _adapter_cpp_header_lines(platform, name)
    lines.extend(_adapter_cpp_lifecycle_lines(name, struct, st))
    lines.extend(
        _adapter_cpp_perform_lines(
            name,
            struct,
            st,
            use_double=use_double,
            io_counts=(len(graph.inputs), len(graph.outputs)),
        )
    )
    lines.extend(_adapter_cpp_introspection_lines(name, struct, st))
    lines.extend(_adapter_cpp_buffers_lines(name))

    return "\n".join(lines)


def generate_manifest_obj(graph: Graph) -> Manifest:
    """
    Generate a ``Manifest`` dataclass from a DSP graph.

    Args:
        graph: The DSP graph.

    Returns:
        A gen-dsp ``Manifest`` instance.
    """
    buffer_ids = [n.id for n in graph.nodes if isinstance(n, Buffer)]
    params = [
        ParamInfo(
            index=i,
            name=p.name,
            has_minmax=True,
            min=p.min,
            max=p.max,
            default=p.default,
        )
        for i, p in enumerate(graph.params)
    ]
    return Manifest(
        gen_name=graph.name,
        num_inputs=len(graph.inputs),
        num_outputs=len(graph.outputs),
        params=params,
        buffers=buffer_ids,
        source="dsp-graph",
        version=__version__,
    )


def generate_manifest(graph: Graph) -> str:
    """
    Generate a ``manifest.json`` compatible with gen-dsp's ``Manifest``.

    Args:
        graph: The DSP graph.

    Returns:
        JSON string.

    """
    buffer_ids = [n.id for n in graph.nodes if isinstance(n, Buffer)]
    data = {
        "gen_name": graph.name,
        "num_inputs": len(graph.inputs),
        "num_outputs": len(graph.outputs),
        "params": [
            {
                "index": i,
                "name": p.name,
                "has_minmax": True,
                "min": p.min,
                "max": p.max,
                "default": p.default,
            }
            for i, p in enumerate(graph.params)
        ],
        "buffers": buffer_ids,
        "source": "dsp-graph",
        "version": __version__,
    }
    return json.dumps(data, indent=2) + "\n"


def compile_for_gen_dsp(
    graph: Graph,
    output_dir: str | Path,
    platform: str,
) -> Path:
    """
    Compile a graph and generate gen-dsp adapter files.

    Writes three files to *output_dir*:

    - ``{name}.cpp`` -- compiled dsp-graph C++
    - ``_ext_{platform}.cpp`` -- gen-dsp adapter
    - ``manifest.json`` -- gen-dsp-compatible manifest

    Args:
        graph: The DSP graph.
        output_dir: Where to write files.
        platform: gen-dsp platform key.

    Returns:
        Path to the output directory.

    """
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    code = compile_graph(graph)
    (out / f"{graph.name}.cpp").write_text(code)

    adapter = generate_adapter_cpp(graph, platform)
    (out / f"_ext_{platform}.cpp").write_text(adapter)

    manifest = generate_manifest(graph)
    (out / "manifest.json").write_text(manifest)

    return out


def generate_graph_build_file(*args: object, **kwargs: object) -> Path:
    """
    Generate a simplified build file for a dsp-graph project.

    Unlike gen~ projects, dsp-graph projects have no genlib dependency,
    no gen/ subdirectory, and no json.c/json_builder.c sources.
    The compiled graph code is included via ``#include`` in the adapter.

    Args:
        args: Legacy positional argument form.
        kwargs: Legacy keyword argument form.
        output_dir: Project directory.
        platform: gen-dsp platform key.
        lib_name: Plugin/external name.
        gen_name: Graph name (used in compile definitions).
        num_inputs: Number of audio inputs.
        num_outputs: Number of audio outputs.
        num_params: Number of parameters.
        genext_version: gen-dsp version string.
        shared_cache: Whether to use shared FetchContent cache.
        midi_defines: Additional MIDI compile definitions.

    Returns:
        Path to the generated build file.

    """
    if args:
        (
            output_dir,
            platform,
            lib_name,
            gen_name,
            num_inputs,
            num_outputs,
            *rest,
        ) = args
        rest_values = [*rest, 0, "0.8.0", False, ""]
        num_params, genext_version, shared_cache, midi_defines = rest_values[:4]
    else:
        output_dir = kwargs["output_dir"]
        platform = kwargs["platform"]
        lib_name = kwargs["lib_name"]
        gen_name = kwargs["gen_name"]
        num_inputs = kwargs["num_inputs"]
        num_outputs = kwargs["num_outputs"]
        num_params = kwargs.get("num_params", 0)
        genext_version = kwargs.get("genext_version", "0.8.0")
        shared_cache = kwargs.get("shared_cache", False)
        midi_defines = kwargs.get("midi_defines", "")

    if not isinstance(output_dir, Path):
        msg = "output_dir must be a Path"
        raise TypeError(msg)

    _build_file_generators = {
        "au": _cmake_au,
        "clap": _cmake_clap,
        "vst3": _cmake_vst3,
        "lv2": _cmake_lv2,
        "sc": _cmake_sc,
        "max": _cmake_max,
        "pd": _makefile_pd,
        "chuck": _makefile_chuck,
        "vcvrack": _makefile_vcvrack,
        "daisy": _makefile_daisy,
        "circle": _makefile_circle,
        "webaudio": _makefile_webaudio,
        "standalone": _makefile_standalone,
        "csound": _makefile_csound,
    }

    generator = _build_file_generators.get(platform)
    if generator is None:
        msg = f"Build file generation not yet supported for platform {platform!r}"
        raise ValueError(msg)

    return generator(
        output_dir=output_dir,
        lib_name=lib_name,
        gen_name=gen_name,
        num_inputs=num_inputs,
        num_outputs=num_outputs,
        num_params=num_params,
        genext_version=genext_version,
        shared_cache=shared_cache,
        midi_defines=midi_defines,
    )


def _cmake_common_header(
    lib_name: str,
    genext_version: str,
    platform: str,
    *,
    shared_cache: bool,
) -> str:
    """Generate common CMake header (project, C++ standard, cache)."""
    use_cache = "TRUE" if shared_cache else "FALSE"

    return f"""\
cmake_minimum_required(VERSION 3.19)

# {platform.upper()} plugin for {lib_name}
# Generated by gen-dsp (dsp-graph source)

set(PROJECT_NAME {lib_name})
project(${{PROJECT_NAME}} VERSION {genext_version})

set(CMAKE_CXX_STANDARD 20)
set(CMAKE_CXX_STANDARD_REQUIRED ON)

if(DEFINED ENV{{GEN_DSP_CACHE_DIR}})
    file(TO_CMAKE_PATH "$ENV{{GEN_DSP_CACHE_DIR}}" _gen_dsp_cache)
    set(FETCHCONTENT_BASE_DIR "${{_gen_dsp_cache}}" CACHE PATH "" FORCE)
elseif({use_cache})
    # Resolve OS-appropriate cache dir at configure time (portable across machines)
    if(APPLE)
        set(_gen_dsp_cache "$ENV{{HOME}}/Library/Caches/gen-dsp/fetchcontent")
    elseif(WIN32)
        if(DEFINED ENV{{LOCALAPPDATA}})
            set(_gen_dsp_cache "$ENV{{LOCALAPPDATA}}/gen-dsp/fetchcontent")
        else()
            set(_gen_dsp_cache "$ENV{{HOME}}/AppData/Local/gen-dsp/fetchcontent")
        endif()
    else()
        if(DEFINED ENV{{XDG_CACHE_HOME}})
            set(_gen_dsp_cache "$ENV{{XDG_CACHE_HOME}}/gen-dsp/fetchcontent")
        else()
            set(_gen_dsp_cache "$ENV{{HOME}}/.cache/gen-dsp/fetchcontent")
        endif()
    endif()
    file(TO_CMAKE_PATH "${{_gen_dsp_cache}}" _gen_dsp_cache)
    set(FETCHCONTENT_BASE_DIR "${{_gen_dsp_cache}}" CACHE PATH "" FORCE)
endif()
"""


