def _copy_if_file(src: Path, dst: Path) -> None:
    if src.is_file():
        shutil.copy2(src, dst)


def _copy_platform_common_templates(
    tmpl_dir: Path, output_dir: Path, platform: str
) -> None:
    for fname in (
        f"gen_ext_{platform}.cpp",
        f"gen_ext_common_{platform}.h",
        f"_ext_{platform}.h",
    ):
        _copy_if_file(tmpl_dir / fname, output_dir / fname)


def _copy_pd_templates(tmpl_dir: Path, output_dir: Path) -> None:
    pd_lib_builder_src = tmpl_dir / "pd-lib-builder"
    if pd_lib_builder_src.is_dir():
        shutil.copytree(
            pd_lib_builder_src,
            output_dir / "pd-lib-builder",
            dirs_exist_ok=True,
        )

    m_pd_src = tmpl_dir / "m_pd.h"
    if m_pd_src.is_file():
        dest = output_dir / "pd-include"
        dest.mkdir(parents=True, exist_ok=True)
        shutil.copy2(m_pd_src, dest / "m_pd.h")


def _copy_chuck_templates(tmpl_dir: Path, output_dir: Path) -> None:
    chugin_src = tmpl_dir / "chuck" / "include" / "chugin.h"
    if chugin_src.is_file():
        dest = output_dir / "chuck" / "include"
        dest.mkdir(parents=True, exist_ok=True)
        shutil.copy2(chugin_src, dest / "chugin.h")

    for mf in ("makefile.mac", "makefile.linux"):
        _copy_if_file(tmpl_dir / mf, output_dir / mf)


def _copy_vcvrack_templates(tmpl_dir: Path, output_dir: Path) -> None:
    for fname in ("plugin.cpp", "plugin.hpp"):
        _copy_if_file(tmpl_dir / fname, output_dir / fname)


def _copy_circle_templates(tmpl_dir: Path, output_dir: Path) -> None:
    for shim in ("cmath", "cstdlib", "cstdint", "cstring"):
        _copy_if_file(tmpl_dir / shim, output_dir / shim)


_PLATFORM_TEMPLATE_COPYERS = {
    "pd": _copy_pd_templates,
    "chuck": _copy_chuck_templates,
    "vcvrack": _copy_vcvrack_templates,
    "circle": _copy_circle_templates,
}


def _copy_platform_templates(output_dir: Path, platform: str) -> None:
    """Copy gen-dsp platform template files to the output directory."""
    getter = getattr(templates, f"get_{platform}_templates_dir", None)
    if getter is None:
        msg = f"No gen-dsp template directory for platform {platform!r}"
        raise ValueError(msg)

    tmpl_dir = getter()
    if not tmpl_dir.is_dir():
        msg = f"Template directory not found: {tmpl_dir}"
        raise FileNotFoundError(msg)

    _copy_platform_common_templates(tmpl_dir, output_dir, platform)

    copier = _PLATFORM_TEMPLATE_COPYERS.get(platform)
    if copier is not None:
        copier(tmpl_dir, output_dir)


