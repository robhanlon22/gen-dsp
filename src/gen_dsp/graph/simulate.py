def _prime_node_state(node: Node, state: dict[str, Any], *, _init: bool) -> None:
    """Populate or reset the state for a node."""
    for node_types, handler in _STATE_PRIME_HANDLERS:
        if isinstance(node, node_types):
            handler(node, state, _init=_init)
            return


@dataclass
class SimResult:
    """Result of a simulation run."""

    outputs: dict[str, NDArray[np.float32]] = field(default_factory=dict)
    state: SimState = field(repr=False, default=None)  # type: ignore[assignment]


@dataclass
class _SimulationRun:
    state: SimState
    inputs: dict[str, NDArray[np.float32]] | None
    ctrl_sorted: list[Node]
    audio_sorted: list[Node]
    ctrl_interval: int
    ctrl_set: set[str]
    held_vals: dict[str, float]


def _resolve_simulation_inputs(
    state: SimState,
    inputs: dict[str, NDArray[np.float32]] | None,
    n_samples: int,
) -> tuple[int, set[str]]:
    """Validate inputs and infer the number of samples."""
    input_ids = {inp.id for inp in state.graph.inputs}
    if inputs:
        lengths = {len(arr) for arr in inputs.values()}
        for iid in inputs:
            if iid not in input_ids:
                msg = f"Unknown input: '{iid}'"
                raise ValueError(msg)
        if len(lengths) > 1:
            msg = f"Input arrays have mismatched lengths: {lengths}"
            raise ValueError(msg)
        inferred = lengths.pop() if lengths else 0
        if n_samples == 0:
            n_samples = inferred
        elif n_samples != inferred:
            msg = f"n_samples={n_samples} does not match input length {inferred}"
            raise ValueError(
                msg
            )
    elif n_samples == 0:
        msg = "n_samples required when no inputs are provided"
        raise ValueError(msg)

    if input_ids and (not inputs or set(inputs.keys()) != input_ids):
        provided = set(inputs.keys()) if inputs else set()
        missing = input_ids - provided
        if missing:
            msg = f"Missing inputs: {missing}"
            raise ValueError(msg)

    return n_samples, input_ids


def _build_sample_values(
    state: SimState,
    inputs: dict[str, NDArray[np.float32]] | None,
    sample_index: int,
) -> dict[str, float]:
    """Build the per-sample value map."""
    vals: dict[str, float] = {}
    if inputs:
        for iid, arr in inputs.items():
            vals[iid] = float(arr[sample_index])
    vals.update(state.params)
    vals["sr"] = state.sr
    for node in state.sorted_nodes:
        if isinstance(node, History):
            vals[node.id] = float(state.state[node.id])
    return vals


def _write_history_values(
    history_nodes: list[History],
    vals: dict[str, float],
    state: SimState,
) -> None:
    """Apply deferred history writes."""
    for h in history_nodes:
        state.state[h.id] = vals[h.input] if isinstance(h.input, str) else h.input


def _capture_control_values(
    ctrl_sorted: list[Node],
    vals: dict[str, float],
    held_vals: dict[str, float],
) -> None:
    """Capture control-rate values for later reuse."""
    for node in ctrl_sorted:
        if node.id in vals:
            held_vals[node.id] = vals[node.id]


def _apply_control_rate_nodes(
    ctrl_sorted: list[Node],
    vals: dict[str, float],
    state: SimState,
    history_nodes: list[History],
) -> None:
    """Compute control-rate nodes and flush their history writes."""
    for node in ctrl_sorted:
        _compute_node(node, vals, state, history_nodes)


def _apply_audio_rate_nodes(
    audio_sorted: list[Node],
    vals: dict[str, float],
    state: SimState,
    history_nodes: list[History],
) -> None:
    """Compute audio-rate nodes and flush their history writes."""
    for node in audio_sorted:
        _compute_node(node, vals, state, history_nodes)


def _simulate_sample(sample_index: int, run: _SimulationRun) -> dict[str, float]:
    """Simulate one sample and return the computed values."""
    vals = _build_sample_values(run.state, run.inputs, sample_index)
    is_ctrl_boundary = (
        run.ctrl_interval > 0 and run.ctrl_set and sample_index % run.ctrl_interval == 0
    )
    if run.ctrl_interval > 0 and run.ctrl_set and not is_ctrl_boundary:
        vals.update(run.held_vals)

    ctrl_history: list[History] = []
    if is_ctrl_boundary:
        _apply_control_rate_nodes(run.ctrl_sorted, vals, run.state, ctrl_history)
        _write_history_values(ctrl_history, vals, run.state)
        _capture_control_values(run.ctrl_sorted, vals, run.held_vals)

    audio_history: list[History] = []
    _apply_audio_rate_nodes(run.audio_sorted, vals, run.state, audio_history)
    _write_history_values(audio_history, vals, run.state)
    return vals


def _resolve_ref(ref: str | float, vals: dict[str, float]) -> float:
    """Resolve a symbolic reference to a float value."""
    if isinstance(ref, float):
        return ref
    return vals[ref]


def _compute_history(
    node: History,
    _vals: dict[str, float],
    _state: SimState,
    history_nodes: list[History],
) -> None:
    history_nodes.append(node)


def _compute_binop(
    node: BinOp,
    vals: dict[str, float],
    _state: SimState,
    _history_nodes: list[History],
) -> None:
    a = _resolve_ref(node.a, vals)
    b = _resolve_ref(node.b, vals)
    op_fn = _BINOP_FUNCTIONS.get(node.op)
    if op_fn is not None:
        vals[node.id] = op_fn(a, b)


def _compute_unary(
    node: UnaryOp,
    vals: dict[str, float],
    state: SimState,
    _history_nodes: list[History],
) -> None:
    a = _resolve_ref(node.a, vals)
    op_fn = _UNARY_FUNCTIONS.get(node.op)
    if op_fn is not None:
        vals[node.id] = op_fn(a, state)


def _compute_clamp(
    node: Clamp,
    vals: dict[str, float],
    _state: SimState,
    _history_nodes: list[History],
) -> None:
    a = _resolve_ref(node.a, vals)
    lo = _resolve_ref(node.lo, vals)
    hi = _resolve_ref(node.hi, vals)
    vals[node.id] = min(max(a, lo), hi)


def _compute_constant(
    node: Constant,
    vals: dict[str, float],
    _state: SimState,
    _history_nodes: list[History],
) -> None:
    vals[node.id] = node.value


