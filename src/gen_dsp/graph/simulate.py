class SimState:
    """Holds all mutable state for a simulated DSP graph."""

    def __init__(self, graph: Graph, sample_rate: float = 0.0) -> None:
        """Build a simulation state for a graph."""
        graph = expand_subgraphs(graph)
        errors = validate_graph(graph)
        if errors:
            raise ValueError("Invalid graph: " + "; ".join(errors))

        self.graph = graph
        self.sr = sample_rate if sample_rate > 0.0 else graph.sample_rate
        self.sorted_nodes = toposort(graph)
        self.params: dict[str, float] = {p.name: p.default for p in graph.params}
        self.state: dict[str, Any] = {}
        self._init_state()

    def _init_state(self) -> None:
        """Initialize node state, mirroring compile.py:_emit_state_init."""
        for node in self.sorted_nodes:
            _prime_node_state(node, self.state, _init=True)

    def reset(self) -> None:
        """Reset all state to initial values, mirroring compile.py:_emit_state_reset."""
        self.params = {p.name: p.default for p in self.graph.params}
        for node in self.sorted_nodes:
            _prime_node_state(node, self.state, _init=False)

    def set_param(self, name: str, value: float) -> None:
        """Set a parameter value. Raises KeyError if name is unknown."""
        if name not in self.params:
            msg = f"Unknown param: '{name}'"
            raise KeyError(msg)
        self.params[name] = value

    def get_param(self, name: str) -> float:
        """Get current parameter value. Raises KeyError if name is unknown."""
        if name not in self.params:
            msg = f"Unknown param: '{name}'"
            raise KeyError(msg)
        return self.params[name]

    def set_buffer(self, buffer_id: str, data: NDArray[np.float32]) -> None:
        """Set buffer contents. Data is truncated/zero-padded to buffer size."""
        key = f"{buffer_id}.buf"
        if key not in self.state:
            msg = f"Unknown buffer: '{buffer_id}'"
            raise KeyError(msg)
        buf: NDArray[np.float32] = self.state[key]
        copy_len = min(len(data), len(buf))
        buf[:copy_len] = data[:copy_len]
        buf[copy_len:] = 0.0

    def get_buffer(self, buffer_id: str) -> NDArray[np.float32]:
        """Get a copy of buffer contents."""
        key = f"{buffer_id}.buf"
        if key not in self.state:
            msg = f"Unknown buffer: '{buffer_id}'"
            raise KeyError(msg)
        buf: NDArray[np.float32] = self.state[key]
        return buf.copy()

    def get_peek(self, peek_id: str) -> float:
        """Get the last value captured by a Peek node."""
        key = f"{peek_id}.value"
        if key not in self.state:
            msg = f"Unknown peek: '{peek_id}'"
            raise KeyError(msg)
        return float(self.state[key])


def _fill_buffer_data(node: Buffer, buf: NDArray[np.float32]) -> None:
    """Fill a buffer using its configured initialization strategy."""
    if node.fill == "sine":
        buf[:] = np.sin(
            2.0 * np.pi * np.arange(node.size, dtype=np.float32) / node.size
        ).astype(np.float32)
    else:
        buf[:] = 0.0


def _prime_history_state(node: History, state: dict[str, Any], *, _init: bool) -> None:
    state[node.id] = node.init


def _prime_delay_line_state(
    node: DelayLine,
    state: dict[str, Any],
    *,
    _init: bool,
) -> None:
    nid = node.id
    if _init:
        state[f"{nid}.buf"] = np.zeros(node.max_samples, dtype=np.float32)
        state[f"{nid}.len"] = node.max_samples
    else:
        buf = state[f"{nid}.buf"]
        buf[:] = 0.0
    state[f"{nid}.wr"] = 0


