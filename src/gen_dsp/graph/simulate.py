def _compute_node(
    node: Node,
    vals: dict[str, float],
    state: SimState,
    history_nodes: list[History],
) -> None:
    """Compute a single node and update values/state in-place."""
    for node_types, handler in _COMPUTE_HANDLERS:
        if isinstance(node, node_types):
            handler(node, vals, state, history_nodes)
            return


def simulate(
    graph: Graph,
    inputs: dict[str, NDArray[np.float32]] | None = None,
    n_samples: int = 0,
    state: SimState | None = None,
    **kwargs: object,
) -> SimResult:
    """
    Simulate a DSP graph in Python, returning output arrays and state.

    Args:
        graph: The DSP graph to simulate.
        inputs: Dict mapping audio input IDs to float32 arrays. All arrays
            must have the same length. May be None for generators.
        n_samples: Number of samples to process. Inferred from inputs if 0.
            Required for generators (no inputs).
        params: Optional param overrides (name -> value).
        state: Optional SimState to reuse across calls. Created if None.
        sample_rate: Sample rate override. Uses graph.sample_rate if 0.
        kwargs: Optional legacy keyword arguments (`params`, `sample_rate`).

    Returns:
        SimResult with output arrays and the (possibly new) SimState.

    Raises:
        ValueError: On invalid graph, mismatched input shapes, etc.

    """
    params = kwargs.pop("params", None)
    sample_rate = kwargs.pop("sample_rate", 0.0)
    if kwargs:
        unexpected = ", ".join(sorted(kwargs))
        msg = f"Unexpected keyword arguments: {unexpected}"
        raise TypeError(msg)

    if state is None:
        state = SimState(graph, sample_rate)

    if params:
        for name, value in params.items():
            state.set_param(name, value)

    n_samples, _input_ids = _resolve_simulation_inputs(state, inputs, n_samples)
    output_arrays: dict[str, NDArray[np.float32]] = {}
    for out in state.graph.outputs:
        output_arrays[out.id] = np.zeros(n_samples, dtype=np.float32)

    ctrl_interval = state.graph.control_interval
    ctrl_set = set(state.graph.control_nodes) if ctrl_interval > 0 else set()
    ctrl_sorted = [n for n in state.sorted_nodes if n.id in ctrl_set]
    audio_sorted = [n for n in state.sorted_nodes if n.id not in ctrl_set]
    held_vals: dict[str, float] = {}
    run = _SimulationRun(
        state=state,
        inputs=inputs,
        ctrl_sorted=ctrl_sorted,
        audio_sorted=audio_sorted,
        ctrl_interval=ctrl_interval,
        ctrl_set=ctrl_set,
        held_vals=held_vals,
    )

    for i in range(n_samples):
        vals = _simulate_sample(i, run)
        for out in state.graph.outputs:
            output_arrays[out.id][i] = np.float32(vals[out.source])

    return SimResult(outputs=output_arrays, state=state)


# ---------------------------------------------------------------------------
# Delay interpolation helpers
# ---------------------------------------------------------------------------


def _interp_linear_delay(
    tap: float,
    buf: NDArray[np.float32],
    length: int,
    wr: int,
) -> float:
    itap = int(tap)
    frac = tap - float(itap)
    i0 = (wr - itap) % length
    i1 = (wr - itap - 1) % length
    s0 = float(buf[i0])
    s1 = float(buf[i1])
    return s0 + frac * (s1 - s0)


def _interp_cubic_delay(
    tap: float,
    buf: NDArray[np.float32],
    length: int,
    wr: int,
) -> float:
    itap = int(tap)
    frac = tap - float(itap)
    i0 = (wr - itap) % length
    im1 = (i0 + 1) % length
    i1 = (wr - itap - 1) % length
    i2 = (wr - itap - 2) % length
    ym1 = float(buf[im1])
    y0 = float(buf[i0])
    y1 = float(buf[i1])
    y2 = float(buf[i2])
    c0 = y0
    c1 = 0.5 * (y1 - ym1)
    c2 = ym1 - 2.5 * y0 + 2.0 * y1 - 0.5 * y2
    c3 = 0.5 * (y2 - ym1) + 1.5 * (y0 - y1)
    return ((c3 * frac + c2) * frac + c1) * frac + c0


# ---------------------------------------------------------------------------
# Buffer interpolation helpers
# ---------------------------------------------------------------------------


def _clamp_buf_idx(idx: int, buf_len: int) -> int:
    if idx < 0:
        return 0
    if idx >= buf_len:
        return buf_len - 1
    return idx


def _interp_linear_buf(
    idx: float,
    buf: NDArray[np.float32],
    buf_len: int,
) -> float:
    i0 = int(idx)
    frac = idx - float(i0)
    i1 = i0 + 1
    i0 = _clamp_buf_idx(i0, buf_len)
    i1 = _clamp_buf_idx(i1, buf_len)
    s0 = float(buf[i0])
    s1 = float(buf[i1])
    return s0 + frac * (s1 - s0)


def _interp_cubic_buf(
    idx: float,
    buf: NDArray[np.float32],
    buf_len: int,
) -> float:
    i0 = int(idx)
    frac = idx - float(i0)
    im1 = _clamp_buf_idx(i0 - 1, buf_len)
    i0c = _clamp_buf_idx(i0, buf_len)
    i1 = _clamp_buf_idx(i0 + 1, buf_len)
    i2 = _clamp_buf_idx(i0 + 2, buf_len)
    ym1 = float(buf[im1])
    y0 = float(buf[i0c])
    y1 = float(buf[i1])
    y2 = float(buf[i2])
    c0 = y0
    c1 = 0.5 * (y1 - ym1)
    c2 = ym1 - 2.5 * y0 + 2.0 * y1 - 0.5 * y2
    c3 = 0.5 * (y2 - ym1) + 1.5 * (y0 - y1)
    return ((c3 * frac + c2) * frac + c1) * frac + c0
