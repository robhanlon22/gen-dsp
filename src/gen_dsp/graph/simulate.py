    buf = state.state[f"{buf_id}.buf"]
    llen: int = state.state[f"{buf_id}.len"]
    ci = max(0.0, min(idx, 1.0))
    fidx = ci * (llen - 1)
    i0 = int(fidx)
    frac = fidx - float(i0)
    i1 = min(i0 + 1, llen - 1)
    vals[node.id] = float(buf[i0]) + frac * (float(buf[i1]) - float(buf[i0]))


